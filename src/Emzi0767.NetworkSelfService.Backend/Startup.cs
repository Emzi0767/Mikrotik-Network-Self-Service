namespace Emzi0767.NetworkSelfService.Backend;

public sealed class Startup
{
    public IConfiguration Configuration { get; set; }

    public Startup(IConfiguration configuration)
    {
        this.Configuration = configuration.SetupConfiguration();
    }

    public void ConfigureServices(IServiceCollection services)
    {
        services.AddHsts(static hsts =>
        {
            hsts.Preload = true;
            hsts.IncludeSubDomains = true;
            hsts.MaxAge = TimeSpan.FromDays(365);
        });

        services.AddOptions<ApplicationConfiguration>()
            .Bind(this.Configuration)
            .ValidateDataAnnotations();

        services.AddOptions<HttpConfiguration>()
            .Bind(this.Configuration.GetSection("Http"))
            .ValidateDataAnnotations();

        services.AddOptions<AuthenticationConfiguration>()
            .Bind(this.Configuration.GetSection("Authentication"))
            .ValidateDataAnnotations();

        services.AddOptions<PostgresConfiguration>()
            .Bind(this.Configuration.GetSection("Postgres"))
            .ValidateDataAnnotations();

        // all other
        services.AddSingleton<TokenKeyProvider>()
            .AddTransient<TokenGenerator>()
            .AddTransient<PasswordHashProvider>()
            .AddTransient<LoginHandler>()
            .AddSingleton<ConnectionStringProvider>()
            .AddHostedService<DatabaseLifetimeService>()
            .AddDbContext<NssDbContext>(ServiceLifetime.Scoped)
            .AddScoped<SessionRepository>()
            .AddScoped<UserRepository>()
            .AddScoped<NetworkRepository>();

        // add auth
        services.AddAuthentication(JwtBearerDefaults.AuthenticationScheme)
            .AddJwtBearer(this.ConfigureJwtAuthentication(this.Configuration));

        services.AddAuthorization(this.ConfigureJwtAuthorization());

        services.AddAntiforgery(opts =>
        {
            opts.HeaderName = "X-XSRF-TOKEN";
            opts.Cookie.Name = "XSRF-TOKEN";
            opts.Cookie.HttpOnly = true;
        });

        services.AddGrpc();
    }

    public void Configure(IApplicationBuilder app, IWebHostEnvironment env)
    {
        if (env.IsDevelopment())
        {
            app.UseDeveloperExceptionPage();
        }
        else
        {
            app.UseExceptionHandler("/error")
                .UseHsts();
        }

        app.UseRouting()
            .UseAuthentication()
            .UseAuthorization()
            .UseAntiforgery()
            .UseEndpoints(static endpoints =>
            {
                endpoints.MapGrpcService<GrpcAuthenticationService>();
                endpoints.MapGet("/", async ctx =>
                {
                    ctx.Response.ContentType = "image/jpeg";
                    ctx.Response.ContentLength = Picard.Length;
                    await ctx.Response.StartAsync();
                    await ctx.Response.BodyWriter.WriteAsync(Picard);
                    await ctx.Response.CompleteAsync();
                });
            });
    }

    private Action<JwtBearerOptions> ConfigureJwtAuthentication(IConfiguration configuration)
    {
        return ConfigureJwtInner;

        void ConfigureJwtInner(JwtBearerOptions jwt)
        {
            var config = new AuthenticationConfiguration();
            configuration.Bind("Authentication", config);

            var keyProvider = new TokenKeyProvider(config);
            jwt.TokenValidationParameters = new()
            {
                IssuerSigningKey = keyProvider.Key,
                ValidIssuer = config.Issuer,
                ValidAudience = config.Audience,
                ValidateIssuerSigningKey = true,
                ValidateIssuer = true,
                ValidateAudience = true,
                RequireSignedTokens = true,
                RequireExpirationTime = true,
                ValidAlgorithms = [ config.Algorithm.ToString() ],
            };
        }
    }

    private Action<AuthorizationOptions> ConfigureJwtAuthorization()
    {
        return ConfigureJwtInner;

        static void ConfigureJwtInner(AuthorizationOptions authorization)
        {
            authorization.AddPolicy(TokenPolicies.AnyUserPolicy, TokenPolicies.AnyUser);
            authorization.AddPolicy(TokenPolicies.RefreshOnlyPolicy, TokenPolicies.RefreshOnly);

            authorization.DefaultPolicy = authorization.GetPolicy(TokenPolicies.AnyUserPolicy);
        }
    }

